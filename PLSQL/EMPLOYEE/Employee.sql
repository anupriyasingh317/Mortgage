CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID      NUMBER PRIMARY KEY,
    NAME             VARCHAR2(100),
    SALARY           NUMBER,
    PERFORMANCE_RATING CHAR(1), -- A, B, C
    HIRE_DATE        DATE,
    DEPARTMENT_ID    NUMBER
);


CREATE TABLE SALARY_AUDIT (
    AUDIT_ID          NUMBER PRIMARY KEY,
    EMPLOYEE_ID       NUMBER,
    OLD_SALARY        NUMBER,
    NEW_SALARY        NUMBER,
    BONUS             NUMBER,
    DEDUCTION         NUMBER,
    PROMOTION_ELIGIBILITY VARCHAR2(10),
    ACTION_DATE       DATE
);


CREATE SEQUENCE SALARY_AUDIT_SEQ START WITH 1 INCREMENT BY 1;
